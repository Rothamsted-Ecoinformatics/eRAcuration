<h3 class="px-7 pt-5 text-2l font-bold text-slate-900">Dataset Listing</h3>
<ul class="list-inside list-disc px-9 pt-5 text-sm text-slate-900">
    <li>The main table lists the datasets and documents in on table.  </li>
    <li>Click on the ID to see the complete information and have access to EDIT, RECYCLE, and NEW from template</li>
    <li>EDIT: Some fields are saves instantly, others need the submit button: so ensure you click on submit to save your changes.</li>
    <li>On the EDIT page, the CANCEL button does not cancel all the changes - sorry</li>
    <li>We don't DELETE, we RECYCLE: Click on RECYCLE if you are not happy with the DOI you have started. </li>
    <li>RECYCLE: this changes the experiment to WASTE BIN - you can do that also through the EDIT experiment button</li>
    <li>I will delete these entries. They are a good place to use as test - you can view what is in the WASTE at URL: <a href="http://local-info.rothamsted.ac.uk/eRA/era2023/experiment/waste#datasets/">http://local-info.rothamsted.ac.uk/eRA/era2023/experiment/waste#datasets/</a> </li>
</ul>

<h3 class="px-7 pt-5 text-2l font-bold text-slate-900">Dataset Show</h3>
<ul class="list-inside list-disc px-9 pt-5 text-sm text-slate-900">
    <li>When you click on an ID on the dataset listing, you get to the SHOW dataset page</li>
    <li>On the left are those fields that have short answers, and on the right, those that need more space</li>
    <li>Fill in the New from TEMPLATE form to create a NEW dataset based on the one you are viewing</li>

</ul>
